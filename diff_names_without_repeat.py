slovar = ["телефон", "арбуз", "телевизор", "интеренет", "телефон", "арбуз", "телевизор", "интеренет", "хоккей",
          "мандарин", "хоккей", "телефон"]
#   новый список с неповторяющимися словами
new_list = []
for i in slovar[::]:
    if i not in new_list:
        new_list.append(i)
# сортировка нового списка в алфавитном порядке
        new_list.sort()
for x in new_list:
    print(x, end=' ')
